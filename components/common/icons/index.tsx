// Type for icon names
export type IconName = keyof typeof Icons;

// Icon component that accepts name prop
interface IconProps {
  name: IconName;
  size?: number;
  color?: string;
  className?: string;
}

export const Icon: React.FC<IconProps> = ({ name, size = 20, color, className }) => {
  const IconComponent = Icons[name];
  if (!IconComponent) return null;
  
  return (
    <span className={className} style={{ color, width: size, height: size, display: 'inline-flex' }}>
      <IconComponent />
    </span>
  );
};

